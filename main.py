def isOne (value):
    if value == 1:
        return True
    return False

def isTen (value):
    if value == 10:
        return True
    return False

def isOneHundred (value):
    if value == 100:
        return True
    return False

def isOneThousand (value):
    if value == 1000:
        return True
    return False

def isTenThousand (value):
    if value == 10000:
        return True
    return False